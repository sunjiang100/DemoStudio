unsigned int		CalZiMo_Addr(unsigned short   cCode)
{
	unsigned int	AB,CD;
	AB = cCode>>8;			// 高字节
	CD = cCode&0x00ff;	// 低字节
	return ( ( (AB-0xa1)*94 + (CD-0xa1) )   *32);
}







