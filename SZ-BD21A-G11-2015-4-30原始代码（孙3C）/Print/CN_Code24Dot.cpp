void			GetCN_Code24_24(unsigned short Data,unsigned char *pData)
{	
	unsigned short		i,Cn,Addr;
	for(i=0;i<CN_CAHR_NUMBER;i++)
	{
		Addr = i;
		Addr <<= 1;
		Cn = GB_CodeForPrint[Addr+1];
		Cn <<= 8;
		Cn |= GB_CodeForPrint[Addr];
		if(Cn==Data)
		{
			Addr = i;
			Addr *= 72;
			memmove(pData,&CN_CodeForPrint[Addr],72);
			return;	
		}
	}
	
}


